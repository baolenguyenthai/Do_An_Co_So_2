    private void btnXemChiTietActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnXemChiTietActionPerformed
        int selectedRow = tblLichSuThi.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một bài thi để xem chi tiết.", "Thông báo",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Lấy thông tin từ model (cột cong_khai ở vị trí 10)
        boolean isCongKhai = (boolean) model.getValueAt(selectedRow, 10);
        if (!isCongKhai) {
            JOptionPane.showMessageDialog(this, "Bộ câu hỏi này không được công khai đáp án.", "Thông báo",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        int baiThiId = (int) model.getValueAt(selectedRow, 0);
        String tenBoCauHoi = (String) model.getValueAt(selectedRow, 1);

        FrmXemChiTietBaiThi frm = new FrmXemChiTietBaiThi(baiThiId, tenBoCauHoi);
        frm.setVisible(true);
    }// GEN-LAST:event_btnXemChiTietActionPerformed

