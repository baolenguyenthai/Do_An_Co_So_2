    public void taiDuLieu() {
        int nguoiDungId = QuanLyPhien.layIdNguoiDungHienTai();
        if (nguoiDungId <= 0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa đăng nhập.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        model.setRowCount(0);
        List<Object[]> rows = baiThiDAO.getLichSuTheoNguoiDung(nguoiDungId);
        for (Object[] row : rows) {
            model.addRow(new Object[] {
                    row[0],
                    row[1],
                    row[2],
                    row[3],
                    row[4],
                    row[5],
                    row[6],
                    formatDateTime(row[7]),
                    formatDateTime(row[8]),
                    formatDuration(row[9]),
                    row[10] // cong_khai (ẩn hoặc dùng để check)
            });
        }
    }

    private String formatDateTime(Object value) {
        if (value == null) {
            return "";
        }
        if (value instanceof java.util.Date) {
            return dateFormat.format((java.util.Date) value);
        }
        return value.toString();
    }

    private String formatDuration(Object secondsObj) {
        if (secondsObj == null) {
            return "";
        }
        long seconds = ((Number) secondsObj).longValue();
        long minutes = seconds / 60;
        long remainSeconds = seconds % 60;
        return minutes + " phút " + remainSeconds + " giây";
    }

