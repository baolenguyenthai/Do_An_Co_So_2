    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String ten = txtTenMonHoc.getText().trim();
        if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên môn học!");
            return;
        }
        MonHoc m = new MonHoc();
        m.setTenMonHoc(ten);
        if (monHocDAO.insert(m)) {
            JOptionPane.showMessageDialog(this, "Thêm thành công!");
            taiDuLieu();
            lamMoi();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại!");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tblMonHoc.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn môn học!");
            return;
        }
        int id = (int) model.getValueAt(row, 0);
        MonHoc m = monHocDAO.getById(id);
        if (m != null) {
            m.setTenMonHoc(txtTenMonHoc.getText().trim());
            if (monHocDAO.update(m)) {
                JOptionPane.showMessageDialog(this, "Sửa thành công!");
                taiDuLieu();
                lamMoi();
            } else {
                JOptionPane.showMessageDialog(this, "Sửa thất bại!");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tblMonHoc.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn môn học!");
            return;
        }
        int id = (int) model.getValueAt(row, 0);
        if (JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa?") == JOptionPane.YES_OPTION) {
            if (monHocDAO.delete(id)) {
                JOptionPane.showMessageDialog(this, "Xóa thành công!");
                taiDuLieu();
                lamMoi();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại!");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

