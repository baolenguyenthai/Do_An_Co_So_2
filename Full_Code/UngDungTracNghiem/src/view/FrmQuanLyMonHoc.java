    private void taiDuLieu() {
        model.setRowCount(0);
        List<MonHoc> list = monHocDAO.getAll();
        for (MonHoc m : list) {
            model.addRow(new Object[]{
                m.getMonHocId(),
                m.getTenMonHoc()
            });
        }
    }

