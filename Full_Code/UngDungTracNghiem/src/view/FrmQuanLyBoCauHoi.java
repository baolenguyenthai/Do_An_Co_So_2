    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String ten = txtTenBo.getText().trim();
        String moTa = txtMoTa.getText().trim();
        if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên bộ câu hỏi!");
            return;
        }
        BoCauHoi b = new BoCauHoi();
        b.setTenBoCauHoi(ten);
        b.setMoTa(moTa);
        b.setCapHocId(((CapHoc) cboCapHoc.getSelectedItem()).getCapHocId());
        b.setMonHocId(((MonHoc) cboMonHoc.getSelectedItem()).getMonHocId());
        b.setNguoiTao(utils.QuanLyPhien.layIdNguoiDungHienTai());
        b.setCongKhai(chkCongKhai.isSelected());
        if (boCauHoiDAO.insert(b)) {
            JOptionPane.showMessageDialog(this, "Thêm thành công!");
            taiDuLieu();
            lamMoi();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại!");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tblBoCauHoi.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn bộ câu hỏi!");
            return;
        }
        int id = (int) model.getValueAt(row, 0);
        BoCauHoi b = boCauHoiDAO.getById(id);
        if (b != null) {
            b.setTenBoCauHoi(txtTenBo.getText().trim());
            b.setMoTa(txtMoTa.getText().trim());
            b.setCapHocId(((CapHoc) cboCapHoc.getSelectedItem()).getCapHocId());
            b.setMonHocId(((MonHoc) cboMonHoc.getSelectedItem()).getMonHocId());
            b.setCongKhai(chkCongKhai.isSelected());
            if (boCauHoiDAO.update(b)) {
                JOptionPane.showMessageDialog(this, "Sửa thành công!");
                taiDuLieu();
            } else {
                JOptionPane.showMessageDialog(this, "Sửa thất bại!");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
    int row = tblBoCauHoi.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn bộ câu hỏi!");
        return;
    }
    int id = (int) model.getValueAt(row, 0);
    BoCauHoi bo = boCauHoiDAO.getById(id);
    if (bo == null) {
        JOptionPane.showMessageDialog(this, "Không tìm thấy bộ câu hỏi!");
        return;
    }
    if (JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa bộ câu hỏi và tất cả dữ liệu liên quan?") == JOptionPane.YES_OPTION) {
        // Xóa cascade: DapAn -> CauHoi -> BoCauHoi -> MonHoc (nếu không dùng chung)
        List<model.CauHoi> cauHois = cauHoiDAO.getByBoCauHoiId(id);
        for (model.CauHoi ch : cauHois) {
            List<model.DapAn> dapAns = dapAnDAO.getByCauHoiId(ch.getCauHoiId());
            for (model.DapAn da : dapAns) {
                dapAnDAO.delete(da.getDapAnId());
            }
            cauHoiDAO.delete(ch.getCauHoiId());
        }
        
        if (boCauHoiDAO.delete(id)) {
            int count = boCauHoiDAO.countByMonHocId(bo.getMonHocId());
            if (count == 0) {
                monHocDAO.delete(bo.getMonHocId());
            }
            JOptionPane.showMessageDialog(this, "Xóa thành công!");
            taiDuLieu();
            lamMoi();
        } else {
            JOptionPane.showMessageDialog(this, "Xóa thất bại!");
        }
    }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnDuyetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDuyetActionPerformed
       int row = tblBoCauHoi.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn bộ câu hỏi!");
            return;
        }
        int id = (int) model.getValueAt(row, 0);
        BoCauHoi b = boCauHoiDAO.getById(id);
        if (b != null) {
            b.setTrangThai(b.getTrangThai().equals("CHO_DUYET") ? "DA_DUYET" : "CHO_DUYET");
            if (boCauHoiDAO.update(b)) {
                JOptionPane.showMessageDialog(this, "Duyệt thành công!");
                taiDuLieu();
            } else {
                JOptionPane.showMessageDialog(this, "Duyệt thất bại!");
            }
        }
    }//GEN-LAST:event_btnDuyetActionPerformed

