    private void tblBoCauHoiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBoCauHoiMouseClicked
    int row = tblBoCauHoi.getSelectedRow();
    if (row != -1) {
        txtTenBo.setText((String) model.getValueAt(row, 1));
        txtMoTa.setText((String) model.getValueAt(row, 2));
        
        // Set selected item cho combo box dựa trên tên
        String tenCapHoc = (String) model.getValueAt(row, 3);
        for (int i = 0; i < cboCapHoc.getItemCount(); i++) {
            if (cboCapHoc.getItemAt(i).getTenCapHoc().equals(tenCapHoc)) {
                cboCapHoc.setSelectedIndex(i);
                break;
            }
        }
        
        String tenMonHoc = (String) model.getValueAt(row, 4);
        for (int i = 0; i < cboMonHoc.getItemCount(); i++) {
            if (cboMonHoc.getItemAt(i).getTenMonHoc().equals(tenMonHoc)) {
                cboMonHoc.setSelectedIndex(i);
                break;
            }
        }
        
        chkCongKhai.setSelected("Có".equals(model.getValueAt(row, 7)));
    }
    }//GEN-LAST:event_tblBoCauHoiMouseClicked

